package uk.co.strangeskies.text.properties;

import static java.lang.annotation.ElementType.TYPE;
import static java.lang.annotation.RetentionPolicy.RUNTIME;

import java.lang.annotation.Retention;
import java.lang.annotation.Target;

/**
 * Annotation for property accessor interfaces to specify how property resources
 * should be fetched to back the implementation of the accessor.
 * 
 * @author Elias N Vasylenko
 */
@Target({ TYPE })
@Retention(RUNTIME)
public @interface Properties {
  /**
   * The location on the classpath of the annotated class at which the properties
   * file can be found.
   */
  String path();
}
